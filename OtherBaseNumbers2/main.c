#include <stdio.h>
int main()
{
    printf("The decimal value of 025 is %d.\n", 025);
    printf("The decimal value of 0x37 is %d.\n", 0x37);
    printf("The decimal value of the letter %c is %d.", 'a', 'a');
    printf("\nThe octal value of the letter %c is %o.", 'a', 'a');
    printf("\nThe hex value of the letter %c is %x.\n", 'a', 'a');

    return 0;
}
